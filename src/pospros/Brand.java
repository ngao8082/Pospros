    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String brand = textbrand.getText().trim();
    String status = combstatus.getSelectedItem().toString();

    if (brand.equals("") || brand.equals("Add brand")) {
        JOptionPane.showMessageDialog(null, "Please enter a valid brand name.");
        return;
    }

    if (status.equals("Add brand")) {
        JOptionPane.showMessageDialog(null, "Please select a valid brand status.");
        return;
    }

    try (Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/possuper", "root", "");
         PreparedStatement selectStatement = connection.prepareStatement("SELECT COUNT(*) FROM brand WHERE brand=?")) {

        // Check if the category already exists in the database
        selectStatement.setString(1, brand);
        ResultSet resultSet = selectStatement.executeQuery();
        resultSet.next();
        int count = resultSet.getInt(1);

        if (count > 0) {
            JOptionPane.showMessageDialog(null, "The brand already exists in the database.");
            return;
        }

        // Insert the category into the database
        try (PreparedStatement insertStatement = connection.prepareStatement("INSERT INTO brand(brand, status) VALUES(?, ?)")) {
            insertStatement.setString(1, brand);
            insertStatement.setString(2, status);
            textbrand.setText(" ");
            combstatus.setSelectedIndex(-1);
            textbrand.requestFocus();
            int rowsAffected = insertStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "brand added successfully.");
                table_update();
                textbrand.setText(" ");
                combstatus.setSelectedIndex(-1);
                textbrand.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Could not add the brand. Please try again.");
            }
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "An error occurred while adding the brand: " + ex.getMessage());
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
