    private void table_update(){
        try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/possuper", "root", "");
        PreparedStatement stmt = conn.prepareStatement("SELECT * FROM brand");
        ResultSet rs = stmt.executeQuery();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // clear existing rows from the table
        while (rs.next()) {
            Object[] row = new Object[3];
            row[0] = rs.getInt("id");
            row[1] = rs.getString("brand");
            row[2] = rs.getString("status");
            model.addRow(row);
        }
        rs.close();
        stmt.close();
        conn.close();
    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(Brand.class.getName()).log(Level.SEVERE, null, ex);
    }
    }

